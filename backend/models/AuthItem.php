    /**
     * Get items
     * @return array
     */
    public function getItems()
    {
        $manager = Configs::authManager();
        $available = [];
        if ($this->type == Item::TYPE_ROLE) {
            foreach (array_keys($manager->getRoles()) as $name) {
                $available[$name] = 'role';
            }
        }
        foreach (array_keys($manager->getPermissions()) as $name) {
            $available[$name] = $name[0] == '/' ? 'route' : 'permission';
        }

        $assigned = [];
        foreach ($manager->getChildren($this->_item->name) as $item) {
            $assigned[$item->name] = $item->type == 1 ? 'role' : ($item->name[0] == '/' ? 'route' : 'permission');
            unset($available[$item->name]);
        }
        unset($available[$this->name]);
        return [
            'available' => $available,
            'assigned' => $assigned,
        ];
    }

    /**
     * Get item
     * @return Item
     */
    public function getItem()
    {
        return $this->_item;
    }

    /**
     * Get type name
     * @param  mixed $type
     * @return string|array
     */
    public static function getTypeName($type = null)
    {
        $result = [
            Item::TYPE_PERMISSION => 'Permission',
            Item::TYPE_ROLE => 'Role',
        ];
        if ($type === null) {
            return $result;
        }

        return $result[$type];
    }

    public function explodeController($listArray){
        $arrReturn = [];

        foreach($listArray as $clac=>$type){
            $flag_admin= strpos($clac, '/admin/') !== false ? true : false;
            $clac     = str_replace('/admin/', '/', $clac);
            $dataClac = array_values(array_filter(explode('/', $clac)));
            $total    = count($dataClac);
            if( $total >= 2 ){
                $controller = $dataClac[0];
                if( $flag_admin )
                    $controller = 'Admin.' . ucfirst($dataClac[0]);
                unset($dataClac[0]);
                $action     = implode('.', $dataClac);
                $arrReturn[$controller][$action] = $type;
                
            }
        }

        return $arrReturn;
    }
}
